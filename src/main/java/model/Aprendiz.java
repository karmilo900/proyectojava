package model;


public class Aprendiz {
    int id;
    int docuape,teleape;
    String nombrea,apellidoa,correoa;

    public Aprendiz() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getDocuape() {
        return docuape;
    }

    public void setDocuape(int docuape) {
        this.docuape = docuape;
    }

    public int getTeleape() {
        return teleape;
    }

    public void setTeleape(int teleape) {
        this.teleape = teleape;
    }

    public String getNombrea() {
        return nombrea;
    }

    public void setNombrea(String nombrea) {
        this.nombrea = nombrea;
    }

    public String getApellidoa() {
        return apellidoa;
    }

    public void setApellidoa(String apellidoa) {
        this.apellidoa = apellidoa;
    }

    public String getCorreoa() {
        return correoa;
    }

    public void setCorreoa(String correoa) {
        this.correoa = correoa;
    }
    
    
}
